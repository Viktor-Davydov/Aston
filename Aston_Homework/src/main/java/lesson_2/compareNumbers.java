package lesson_2;

public class compareNumbers {
    public static void main(String[] args) {
        compareNumbers();
    }
    public static void compareNumbers() {
        int a = 2;
        int b = 6;
        if (a >= b) {
            System.out.print("a >= b");
        }
        else {
            System.out.print("a < b");
        }
    }
}